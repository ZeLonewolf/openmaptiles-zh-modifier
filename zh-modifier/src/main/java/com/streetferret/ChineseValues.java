package com.streetferret;

public class ChineseValues {
	private long id;

	private String hant;
	private String hans;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getHant() {
		return hant;
	}

	public void setHant(String hant) {
		this.hant = hant;
	}

	public String getHans() {
		return hans;
	}

	public void setHans(String hans) {
		this.hans = hans;
	}
}
